				<h3>Daegu city tour avec Sarah -- 8 mars 2015</h3>
				<p>
				A Daegu il est possible de faire différents tour en bus. J'ai visité Bullo-Dong Tomb Park, Daegu Bangjja Yugi Museum, Donghwasa Temple et un parc à thème sur la sécurité du métro.
				</p>
				<h4>Bullo-Dong Tomb Park</h4>
				<p>
				Telle deux aventurières Sarah et moi prenons le bus jusqu'à Dongdaegu Station. C'est la gare routière de la ville. Là on prend notre ticket pour le daegu city tour et on rejoint quelques coréens, une finlandaise et un malaisien d'origine chinoise.</br>
				<a href="photo/coree.city1.jpg"><img src="photo/coree/tn_city1.jpg" alt="photo city tour" title="Cliquez pour agrandir"></a>
				<a href="photo/coree.city2.jpg"><img src="photo/coree/tn_city2.jpg" alt="photo city tour" title="Cliquez pour agrandir"></a>
				<a href="photo/coree.city3.jpg"><img src="photo/coree/tn_city3.jpg" alt="photo city tour" title="Cliquez pour agrandir"></a><br />
				La guide a un anglais très approximatif...</br>
				On arrive au premier arrêt Bullo-Dong Tomb Park. Là une autre guide arrive et elle aussi ne parle que coréen. De ce fait une maman coréenne nous fait quelques traductions, ce qui permet de suivre et de savoir que les jolies collines sont en fait des tombes. Les coréens aiment cet endroit car c'est un vestige de leur civilisation. Les tombes datent du 5ème - 6ème Siècles après JC</br>
				En tout il y a plus de 700 tombes dont 214 qui ont été restaurées et mon université (KNU) participe à leurs étude.</br>
