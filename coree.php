				<p>
				Ce week end a été plutôt spécial. J'ai participé à une course en stop. Le but? Arriver en premier au temple de Samgwangsa en partant de Daegu. Les participants de cette course ont eu le droit d'utiliser un ticket de bus et le reste du voyage devait être fait en stop!!
