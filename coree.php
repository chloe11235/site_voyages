				<h4>Départ de Daegu en stop</h4>
				<p>Le meilleur moyen pour aller à Busan c'est ... le stop!</br>
				100% de réussite pour le moment.</br></br>
				Avec Sarah (comme d'habitude) On se lève le matin et on part avec le bus 937 pour l'autoroute. On se place au niveau d'une aire de repos (bien avant le péage) et on attend.</br>
				Au bout de 5 min une voiture met son clignotant, nous fait signe et se gare!! Mais pas pour nous prendre, juste pour se garer. Le signe de main voulait dire non. Petite deception...</br>
				Au bout de 20 min on part à la recherche d'un meilleur endroit pour ensuite revenir à notre aire de repos. Et là une voiture s'arrête pour nous prendre.</br>
				Les deux dames de la voiture nous ont nourris avec du kimbap à midi et elles nous ont déposées à la station de bus/métro en périphérie de Busan pour qu'on aille facilement dans le centre ville car elles n'y allaient pas. Ces deux dames ont été extrèmement gentille!!</br>
				On a passé l'apres-midi dans Busan, dans un genre de marché avec énormément de street food à base de poisson (crabe, coquillages, concombre de mer ...)et on a rejoint les autres.</br>
				</p>
				<h4>La guest-house</h4>
				<p>
				Franchement dormir quelque part pour moins de 13€ avec petit déjeuner, draps et douche du matin compris ça me va.</br>
				En plus le petit-déjeuner s'est déroulé tout en douceur sur un fond de guitare et de chansons. =) Que demander de plus?
				</p>
				<h4>Le festival</h4>
				<p>
				La ça devient vraiment intéressant. Tout d'abord on a du aller chercher nos chapeaux et nos sachets de poudre colorée ainsi que des samoussas! On s'est ensuite tranquillement installé sur la plage pas loin du dj qui passait de l'indi-électro avec des mamies coréennes qui mettaient l'ambiance en dansant à côté!</br>
				L'attente avant de lancer les couleurs en l'air est très, très longue...</br>
				Ca y est, c'est parti! Lancer de couleur en l'air, sur les gens, sur les amis, sur les inconnus. on reçoit des couleurs surtout par les indiens, les autres nationalités garde leur pourdre colorée précieusement.</br>
				On tombe vite à cours de poudre et la peinture vient la remplacer.</br>
				Après avoir bien été recouvert de peinture et de poudre, un petit passage dans la mer s'impose. Et elle est super froide, mais moins que l'eau des douches à disposition. brrr </br>
				Vu notre état (de la peinture plein les cheveux et sur mon sac) j'abandonne l'idée de rentrer en stop et le retour se fait en bus. Mais attention en bus de luxe! Siège allongeable et repose pied. =)
				</p>
