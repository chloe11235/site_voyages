				Avec Sohib, on a préparé de magnifiques pancartes de stop. Le jour j on part à la recherche d'une voiture susceptible de nous prendre et au bout de 5 minutes on voit des français à bord d'une voiture en route pour Busan. Mais comment ont-ils fait ??</br>
				On avance et marche pendant un petit moment et on voit une voiture arretée, prête à partir. On parle au conducteur et au bout d'un moment le gars descent et continue de nous parler. On essaye de le convaincre de nous emmener à Busan. Et ... C'est bon, il nous emmène!</br>
				Pendant le trajet on joue aux cartes, on discute et enfin on arrive. Le chauffeur est tellement gentil qu'il nous emmène au poed du temple, situé en haut d'une montée. On y arrive et il n'y  personne à l'horizon. On a gagné!! Et là 3 personnes débarques, elles étaient là depuis 30-40 min. Bon on est arrivé et on s'est bien marré =)
