				<h4>Daegu Bangjja Yugi Museum</h4>
				<p>
				On reprend ensuite le bus pour arriver dans un musée du "bol".</br>
				En fait le musée montre comment sont fabriqués artisanalement les gongs, bols, et autres contenants. Ces objets sont fabriqué avec du cuivre et de l'étain.</br>
				Il y a trois méthodes de fabrication différentes et l'une consiste à chauffer, taper, chauffer, taper, chauffer, taper, plonger l'objet dans un bain d'eau froide, taper.</br>
				Ce musée n'était pas intéressant sans doute parce que toutes les explications, films, panneaux étaient en Coréen. Sauf le film du début qui lui était soustitré anglais et beaucoup trop enthousiaste pour être crédible. Comme pour le film de présentation de knu la vidéo resemblait plus à une bande annonce de film ou à la présentation d'un trésor national d'une valeur inestimable qu'à un film de présentation d'un art ancestral coréen.<br />		
