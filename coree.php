				<h4>Donghwasa Temple</h4>
				<p>
				De retour dans le bus on part pour le temple. C'est pour pourvoir le visiter que l'on a choisi ce voyage en bus.</br>
				Avant de visiter le temple c'est la pause déjeuner et je déguste alors un bibimbap. C'est un plat coréen avec du riz et plein de choses dedans, des légumes et autre. C'est pas mal et avec Sarah on profite du repas pour discuter avec la finlandaise et le malaisien. Ce dernier est d'origine chinoise et il nous explique comment les traditions chinoises se sont perdu dans sa famille en trois générations. Il estime que il reste un petit 10% de tradition chinoise dans la vie de son fils.</br>
				<a href="photo/coree/city7.jpg"><img .aligncenter src="photo/coree/tn_city7.jpg" alt="photo city tour" title="Cliquez pour agrandir"></a><br />	
				On part enfin pour le temple.  Il est vraiment magnifique. Normalement il faut payer un droit d'entrée mais aujourd'hui est un jour spécial et le temple est gratuit. </br>
				<a href="photo/coree/city8.jpg"><img src="photo/coree/tn_city8.jpg" alt="photo city tour" title="Cliquez pour agrandir"></a><br />	
				Le temple est morcelé en plusieurs endroits dans la montagne. Pour arriver dans un endroit avec plein de batiment il faut suivre un long chemin avec énormément de lanterne de chaque côté. C'est vraiment sympa de marcher dans cette ambiance. On découvre les fontaines auquels de nombreux coréens s'abreuvent. Malheuresement notre guide ne pas nous expliquer pourquoi ils font ça :(</br>
				On peut ainsi monter jusqu'à l'endroit où trois boudhas sont alignés: un pour le passé, un pour le présent et un pour le futur.</br>
				On continue notre ballade, on passe sur un pont qui enjambe une rivière à moitié gelée. On arrive ensuite devant un boudha géant, entourée par des figures sculptées dans le mur tout autour de lui. Cet endroit est juste impressionant.</br>
				On passe devant des stands où l'on peut acheter des tuiles et marquer un mot dessus. Ces tuiles serviront à restaurer les toits des différents batiments du temple.</br>
				La visite finie (enfin la balade, on a vu plein de jolies choses sans savoir à quoi elles correspondaient) on part ensuite pour le par à thème et la guide s'affole car on a perdu le malaisien.
				</P>
				<h4>Le parc à thème</h4>
				<p>
				On rejoint la famille coréenne avec la maman qui sait parler anglais.</br>
				Avec Sarah On a eu droit à un film sur l'accident de métro qui a fait 193 morts à Daegu en 2003. Un homme, complètement fou, a mis le feu à un bidon d'essence et a tué tous ces gens à lui tout seul, dont une cinquantaine qui n'ont pas réussis à sortir à temps de la station. Les autres sont restés bloqués dans les wagons.</br>
				Toutes les personnes présentes ont pris place dans des sièges comme si on allait faire un ciné dinamique. Tout d'un coup la plateforme sur laquelle on était assis commence à descendre et s'arrête un bon 10-15 mètres plus bas.</br>
				La vidéo commence (en coréen bien sur) et on voit une petite fille partir à l'école avec son portable. Elle passe des appels à son papa jusqu'à l'accident. C'est le genre de film auquel on ne s'attendait pas du tout. Les enfants de 4-5 ans n'ont pas l'air trop traumatisés.</br>
				Ensuite on sort et on arrive dans une reconstitution d'une station de métro après l'acident. Le vrai wagon y était exposé. On a de nouveaux droits à des explications en coréen et de nouveau la maman nous fait la traduction et je transmet à la finlandaise.</br>
				Ensuite on fait une simulation d'évacuation d'un métro en feu. C'était assez impressionant de ne plus rien voir à cause de la fumée.
				</br>
				</br>
				On fini par rentrer à knu, c'était une bonne petite ballade, interessante.
				</p>
